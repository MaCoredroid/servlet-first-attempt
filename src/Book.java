public class Book{
    public String name=" ";
    public String author=" ";
    public double price= 0;
    public String isbn = " ";
    public int stock=0;
    public String img = " ";
    Book(String name,String author, double price ,String isbn,int stock,String img)
    {
        this.name = name;
        this.author= author;
        this.price= price;
        this.isbn = isbn;
        this.stock = stock;
        this.img = img;
    }






}
